public class Sun{
  private int momentum = 0;
  private int gravity = 0;
  
  public void setMomentum (int m){
    momentum = m;
  }
  
  public void setGravity (int g){
    gravity = g;
  }
  
  public int getMomentum(){
    return momentum;
  }
  
  public int getGravity(){
    return gravity;
  }
}